def _add_speaker_and_signal(header, source, get_conversation=True):
    """Add speaker and start/end signal on each round."""
    BEGIN_SIGNAL = "### "
    END_SIGNAL = "\n"
    conversation = header
    for sentence in source:
        from_str = sentence["from"]
        if from_str.lower() == "human":
            from_str = conversation_lib.default_conversation.roles[0]
        elif from_str.lower() == "gpt":
            from_str = conversation_lib.default_conversation.roles[1]
        else:
            from_str = 'unknown'
        sentence["value"] = (BEGIN_SIGNAL + from_str + ": " +
                             sentence["value"] + END_SIGNAL)
        if get_conversation:
            conversation += sentence["value"]
    conversation += BEGIN_SIGNAL
    return conversation

class TextPreprocessor():
    def __init__(
        self,
        default_conversation: conversation_lib.Conversation,
        tokenizer,
        is_multimodal,
        mm_use_im_start_end,
        ) -> None:
        self.default_conversation = default_conversation
        self.tokenizer = tokenizer
        self.is_multimodal = is_multimodal
        self.mm_use_im_start_end = mm_use_im_start_end

    def __call__(
            self, 
            sources,
            has_image,
        ) -> Any:
        if has_image:
            if not self.is_multimodal:
                return sources

            for sentence in sources:
                if DEFAULT_IMAGE_TOKEN in sentence['value']:
                    sentence['value'] = sentence['value'].replace(DEFAULT_IMAGE_TOKEN, '').strip()
                    sentence['value'] = DEFAULT_IMAGE_TOKEN + '\n' + sentence['value']
                    sentence['value'] = sentence['value'].strip()
                    if "mmtag" in conversation_lib.default_conversation.version:
                        sentence['value'] = sentence['value'].replace(DEFAULT_IMAGE_TOKEN, '<Image>' + DEFAULT_IMAGE_TOKEN + '</Image>')
                replace_token = DEFAULT_IMAGE_TOKEN
                if self.mm_use_im_start_end:
                    replace_token = DEFAULT_IM_START_TOKEN + replace_token + DEFAULT_IM_END_TOKEN
                sentence["value"] = sentence["value"].replace(DEFAULT_IMAGE_TOKEN, replace_token)

        if self.default_conversation.sep_style == conversation_lib.SeparatorStyle.PLAIN:
            return preprocess_plain(sources, self.tokenizer)
        if self.default_conversation.sep_style == conversation_lib.SeparatorStyle.LLAMA_2:
            return preprocess_llama_2(sources, self.tokenizer, has_image=has_image)
        if self.default_conversation.version.startswith("v1"):
            return preprocess_v1(sources, self.tokenizer, has_image=has_image)
        if self.default_conversation.version == "mpt":
            return preprocess_mpt(sources, self.tokenizer, has_image=has_image)
        # add end signal and concatenate together
        conversations = []
        for source in sources:
            header = f"{self.default_conversation.system}\n\n"
            conversation = _add_speaker_and_signal(header, source)
            conversations.append(conversation)
        # tokenize conversations
        def get_tokenize_len(prompts):
            return [len(tokenizer_image_token(prompt, self.tokenizer)) for prompt in prompts]

        if has_image:
            input_ids = [tokenizer_image_token(prompt, self.tokenizer, return_tensors='pt') for prompt in conversations]
        else:
            conversations_tokenized = _tokenize_fn(conversations, self.tokenizer)
            input_ids = conversations_tokenized["input_ids"]

        targets = copy.deepcopy(input_ids)
        for target, source in zip(targets, sources):
            if has_image:
                tokenized_lens = get_tokenize_len([header] + [s["value"] for s in source])
            else:
                tokenized_lens = _tokenize_fn([header] + [s["value"] for s in source], self.tokenizer)["input_ids_lens"]
            speakers = [sentence["from"] for sentence in source]
            _mask_targets(target, tokenized_lens, speakers)


        return targets